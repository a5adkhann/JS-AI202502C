let myDigit = 5;

switch(myDigit){
    case 10:
        console.log("Case 1");
        break;
    case 5:
        console.log("Case 2");
        break;
    case 10:
        console.log("Case 3");
        break;
    default:
        console.log("My Default Statement")
}